import  java.util.Scanner;
class Loop2
{
	public static void main (String [] args)
	{
		Scanner sc=new Scanner( System.in);
		System.out.println("ENTER THE START NUMBER ");
		int num = sc.nextInt();
		int i=1;
	

		do 
		{
			System.out.println(num +"x " + i + "=" +(i*5));
			i++;
			
			
		}while (i<=10);
		
	
	 
	
	
	
	
	
	
	
    }
}
		
		
